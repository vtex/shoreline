import { mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import path from 'node:path'
import ts from 'typescript'

const root = process.cwd()
const manifestPath = path.join(root, 'code-connect/shoreline-components.json')
const outputDir = path.join(root, 'code-connect/generated')

const manifest = JSON.parse(await readFile(manifestPath, 'utf8'))
const figmaFileUrl = manifest.figmaFileUrl

const components = (manifest.components ?? []).map((component) => ({
  ...component,
  id: validateComponentId(component.id),
}))
const componentIds = components.map((component) => component.id)
const duplicateComponentIds = [
  ...new Set(
    componentIds.filter((id, index) => componentIds.indexOf(id) !== index)
  ),
]

if (duplicateComponentIds.length > 0) {
  throw new Error(
    `Duplicate Code Connect component id${duplicateComponentIds.length === 1 ? '' : 's'} found: ${duplicateComponentIds.map(String).join(', ')}`
  )
}

const readyComponents = components.filter((component) =>
  isPublishableFigmaUrl(getFigmaUrl(component))
)
const missingComponents = components.filter(
  (component) => !isPublishableFigmaUrl(getFigmaUrl(component))
)

await rm(outputDir, { recursive: true, force: true })
await mkdir(outputDir, { recursive: true })

await Promise.all(
  readyComponents.map(async (component) =>
    writeFile(
      path.join(outputDir, `${component.id}.figma.ts`),
      await createTemplate(component, getFigmaUrl(component))
    )
  )
)

console.log(
  `Generated ${readyComponents.length} Code Connect mapping${readyComponents.length === 1 ? '' : 's'}.`
)

if (missingComponents.length > 0) {
  console.log(
    `Skipped ${missingComponents.length} component${missingComponents.length === 1 ? '' : 's'} without Figma node URLs.`
  )
}

async function createTemplate(component, figmaUrl) {
  const dynamicTemplate = createDynamicTemplate(component)
  const exampleTemplate = await createExampleTemplate(
    component,
    dynamicTemplate
  )
  const imports = exampleTemplate.imports ??
    component.imports ?? [component.component]
  const importStatements = createImportStatements(imports)
  const props = `${escapeTemplateLiteral(component.props ?? '')}${dynamicTemplate.props}`
  const children =
    dynamicTemplate.children ??
    escapeTemplateLiteral(component.children ?? 'Children')

  if (exampleTemplate.hasExplicitExample && dynamicTemplate.hasDynamicParts) {
    throw new Error(
      `Code Connect component ${component.id} cannot use example, exampleFile or exampleVariants with figmaProps or figmaChildren.`
    )
  }

  const prelude = [dynamicTemplate.prelude, exampleTemplate.prelude]
    .filter(Boolean)
    .join('\n')
  const formattedPrelude = prelude ? `\n${prelude}` : ''
  const example =
    exampleTemplate.expression ??
    createExample(component.component, props, children, component.selfClosing)

  return `/// <reference types="@figma/code-connect/figma-types" />
// This file is generated by scripts/generate-code-connect.mjs.
// Update code-connect/shoreline-components.json instead.
// url=${figmaUrl}
// source=${component.source}
// component=${component.component}
import figma from 'figma'
${formattedPrelude}
export default {
  imports: ${createImports(importStatements)},
  example: ${example},
  id: '${component.id}',
  metadata: {
    nestable: ${component.nestable === true},
  },
}
`
}

function createImportStatements(imports) {
  if (
    imports.length > 0 &&
    imports.every((item) => item.startsWith('import '))
  ) {
    return imports
  }

  return [`import { ${imports.join(', ')} } from '@vtex/shoreline'`]
}

function createImports(importStatements) {
  if (importStatements.length === 1 && importStatements[0].length <= 70) {
    return `[${JSON.stringify(importStatements[0])}]`
  }

  return `[
${importStatements.map((statement) => `    ${JSON.stringify(statement)},`).join('\n')}
  ]`
}

async function createExampleTemplate(component, dynamicTemplate) {
  const explicitExamples = [
    component.example,
    component.exampleFile,
    component.exampleVariants,
  ].filter((value) => value !== undefined)

  if (explicitExamples.length > 1) {
    throw new Error(
      `Code Connect component ${component.id} can only use one of example, exampleFile or exampleVariants.`
    )
  }

  if (component.example) {
    return {
      expression: createCodeExpression(component.example),
      hasExplicitExample: true,
      imports: undefined,
      prelude: '',
    }
  }

  if (component.exampleFile) {
    const docsExample = await loadDocsExample(component.exampleFile)

    return {
      expression: createCodeExpression(docsExample.code),
      hasExplicitExample: true,
      imports: docsExample.imports,
      prelude: '',
    }
  }

  if (component.exampleVariants) {
    if (dynamicTemplate.hasDynamicParts) {
      throw new Error(
        `Code Connect component ${component.id} cannot use exampleVariants with figmaProps or figmaChildren.`
      )
    }

    return createVariantExampleTemplate(component.id, component.exampleVariants)
  }

  return {
    expression: undefined,
    hasExplicitExample: false,
    imports: undefined,
    prelude: '',
  }
}

async function createVariantExampleTemplate(componentId, exampleVariants) {
  if (!Array.isArray(exampleVariants) || exampleVariants.length === 0) {
    throw new Error(
      `Code Connect component ${componentId} exampleVariants must be a non-empty array.`
    )
  }

  const examples = await Promise.all(
    exampleVariants.map(async (variant) => ({
      ...variant,
      docsExample: await loadDocsExample(variant.exampleFile),
    }))
  )
  const imports = [
    ...new Set(examples.flatMap((variant) => variant.docsExample.imports)),
  ]
  const conditionEntries = examples.flatMap((variant) =>
    Object.entries(variant.when ?? {})
  )
  const conditionProps = new Map()

  for (const [propName, value] of conditionEntries) {
    const prop = conditionProps.get(propName) ?? {
      kind: typeof value === 'boolean' ? 'boolean' : 'enum',
      values: new Set(),
    }

    if (
      (prop.kind === 'boolean' && typeof value !== 'boolean') ||
      (prop.kind === 'enum' && typeof value === 'boolean')
    ) {
      throw new Error(
        `Code Connect component ${componentId} exampleVariants cannot mix boolean and enum conditions for ${propName}.`
      )
    }

    prop.values.add(value)
    conditionProps.set(propName, prop)
  }

  const usedIdentifiers = new Set(['figma', 'instance', 'example'])
  const propIdentifiers = new Map()
  const preludeLines = ['const instance = figma.selectedInstance']

  for (const [propName, prop] of conditionProps) {
    const identifier = createIdentifier(usedIdentifiers, propName, 'Value')
    propIdentifiers.set(propName, identifier)

    if (prop.kind === 'boolean') {
      preludeLines.push(
        `const ${identifier} = instance.getBoolean(${createStringLiteral(propName)})`
      )
    } else {
      const values = Object.fromEntries(
        [...prop.values].map((value) => [String(value), String(value)])
      )
      preludeLines.push(
        `const ${identifier} = instance.getEnum(${createStringLiteral(propName)}, ${createObjectLiteral(values)})`
      )
    }
  }

  const expressionLines = createExampleTernaryLines(examples, propIdentifiers)

  preludeLines.push('', ...expressionLines)

  return {
    expression: 'example',
    hasExplicitExample: true,
    imports,
    prelude: `${preludeLines.join('\n')}\n`,
  }
}

function createExampleTernaryLines(examples, propIdentifiers) {
  const [firstExample] = examples
  const firstCondition = createExampleCondition(
    firstExample.when,
    propIdentifiers
  )

  if (!firstCondition) {
    if (examples.length > 1) {
      throw new Error('Only the last exampleVariants entry can omit when.')
    }

    return [
      `const example = ${createCodeExpression(firstExample.docsExample.code)}`,
    ]
  }

  const lines = ['const example =']

  examples.forEach((variant, index) => {
    const condition = createExampleCondition(variant.when, propIdentifiers)
    const conditionIndent = '  '.repeat(index + 1)
    const valueIndent = '  '.repeat(index + 2)
    const expression = createCodeExpression(variant.docsExample.code)

    if (!condition) {
      if (index !== examples.length - 1) {
        throw new Error('Only the last exampleVariants entry can omit when.')
      }

      lines.push(`${conditionIndent}: ${expression}`)
      return
    }

    lines.push(
      index === 0
        ? `${conditionIndent}${condition}`
        : `${conditionIndent}: ${condition}`,
      `${valueIndent}? ${expression}`
    )
  })

  return lines
}

function createExampleCondition(when, propIdentifiers) {
  const entries = Object.entries(when ?? {})

  if (entries.length === 0) {
    return ''
  }

  return entries
    .map(([propName, value]) => {
      const identifier = propIdentifiers.get(propName)

      if (!identifier) {
        throw new Error(`Missing example variant identifier for ${propName}.`)
      }

      return `${identifier} === ${
        typeof value === 'boolean' ? String(value) : createStringLiteral(value)
      }`
    })
    .join(' && ')
}

async function loadDocsExample(exampleFile) {
  const { filePath, root } = normalizeExampleFile(exampleFile)
  const source = await readFile(filePath, 'utf8')
  const sourceFile = ts.createSourceFile(
    filePath,
    source,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TSX
  )
  const shorelineImports = getShorelineImports(sourceFile)
  const returnExpression = getDefaultReturnExpression(sourceFile)
  const exampleNode = root
    ? findJsxElement(returnExpression, root)
    : unwrapExpression(returnExpression)

  if (!exampleNode) {
    throw new Error(
      `Could not find ${root ?? 'default example'} in ${path.relative(rootDir(), filePath)}.`
    )
  }

  const usedComponents = getUsedJsxComponents(exampleNode)
  const imports = [...usedComponents].filter((component) =>
    shorelineImports.has(component)
  )
  const missingImports = [...usedComponents].filter(
    (component) => !shorelineImports.has(component)
  )

  if (missingImports.length > 0) {
    throw new Error(
      `Docs example ${path.relative(rootDir(), filePath)} uses non-Shoreline component${missingImports.length === 1 ? '' : 's'}: ${missingImports.join(', ')}`
    )
  }

  return {
    code: exampleNode.getText(sourceFile),
    imports,
  }
}

function normalizeExampleFile(exampleFile) {
  const config =
    typeof exampleFile === 'string' ? { path: exampleFile } : exampleFile

  if (typeof config?.path !== 'string' || config.path.length === 0) {
    throw new Error('Code Connect exampleFile entries need a path string.')
  }

  const rootPath = rootDir()
  const filePath = path.resolve(rootPath, config.path)

  if (!filePath.startsWith(`${rootPath}${path.sep}`)) {
    throw new Error(
      `Code Connect exampleFile is outside the repository: ${config.path}`
    )
  }

  return {
    filePath,
    root: config.root,
  }
}

function getShorelineImports(sourceFile) {
  const imports = new Set()

  sourceFile.forEachChild((node) => {
    if (
      !ts.isImportDeclaration(node) ||
      !ts.isStringLiteral(node.moduleSpecifier) ||
      node.moduleSpecifier.text !== '@vtex/shoreline'
    ) {
      return
    }

    const namedBindings = node.importClause?.namedBindings

    if (!namedBindings || !ts.isNamedImports(namedBindings)) {
      return
    }

    for (const element of namedBindings.elements) {
      imports.add(element.name.text)
    }
  })

  return imports
}

function getDefaultReturnExpression(sourceFile) {
  let expression

  function visit(node) {
    if (
      ts.isFunctionDeclaration(node) &&
      node.modifiers?.some(
        (modifier) => modifier.kind === ts.SyntaxKind.DefaultKeyword
      ) &&
      node.body
    ) {
      const returnStatement = node.body.statements.find(ts.isReturnStatement)
      expression = returnStatement?.expression
      return
    }

    ts.forEachChild(node, visit)
  }

  visit(sourceFile)

  if (!expression) {
    throw new Error(
      `Could not find a default function return in ${path.relative(rootDir(), sourceFile.fileName)}.`
    )
  }

  return expression
}

function unwrapExpression(node) {
  return ts.isParenthesizedExpression(node)
    ? unwrapExpression(node.expression)
    : node
}

function findJsxElement(node, rootName) {
  const unwrappedNode = unwrapExpression(node)

  if (isJsxTagNamed(unwrappedNode, rootName)) {
    return unwrappedNode
  }

  let match

  function visit(child) {
    if (match) {
      return
    }

    if (isJsxTagNamed(child, rootName)) {
      match = child
      return
    }

    ts.forEachChild(child, visit)
  }

  ts.forEachChild(unwrappedNode, visit)

  return match
}

function isJsxTagNamed(node, name) {
  if (ts.isJsxElement(node)) {
    return getJsxTagName(node.openingElement.tagName) === name
  }

  if (ts.isJsxSelfClosingElement(node)) {
    return getJsxTagName(node.tagName) === name
  }

  return false
}

function getUsedJsxComponents(node) {
  const components = new Set()

  function visit(child) {
    const tagName = ts.isJsxElement(child)
      ? getJsxTagName(child.openingElement.tagName)
      : ts.isJsxSelfClosingElement(child)
        ? getJsxTagName(child.tagName)
        : undefined

    if (tagName && /^[A-Z]/.test(tagName)) {
      components.add(tagName)
    }

    ts.forEachChild(child, visit)
  }

  visit(node)

  return components
}

function getJsxTagName(tagName) {
  if (ts.isIdentifier(tagName)) {
    return tagName.text
  }

  return undefined
}

function createCodeExpression(code) {
  return `figma.code\`${escapeTemplateLiteral(code)}\``
}

function rootDir() {
  return root
}

function createDynamicTemplate(component) {
  const figmaProps = component.figmaProps ?? []
  const hasFigmaChildren = component.figmaChildren !== undefined
  const hasDynamicParts = figmaProps.length > 0 || hasFigmaChildren

  if (!hasDynamicParts) {
    return { children: undefined, hasDynamicParts, prelude: '', props: '' }
  }

  if (component.selfClosing && hasFigmaChildren) {
    throw new Error(
      `Code Connect component ${component.id} cannot use figmaChildren with selfClosing.`
    )
  }

  const usedIdentifiers = new Set(['figma', 'instance'])
  const dynamicPropLines = []
  const dynamicPropInterpolations = []
  let usesStringPropHelper = false

  for (const figmaProp of figmaProps) {
    validateFigmaProp(component.id, figmaProp)

    const valueIdentifier = createIdentifier(
      usedIdentifiers,
      figmaProp.variable ?? figmaProp.prop,
      'Value'
    )
    const propIdentifier = createIdentifier(
      usedIdentifiers,
      figmaProp.variable ?? figmaProp.prop,
      'Prop'
    )

    if (figmaProp.kind === 'enum') {
      usesStringPropHelper = true
      dynamicPropLines.push(
        `const ${valueIdentifier} = instance.getEnum(${createStringLiteral(figmaProp.figmaProp)}, ${createEnumOptions(figmaProp)})`
      )
      dynamicPropLines.push(
        `const ${propIdentifier} = createStringProp(${createStringLiteral(figmaProp.prop)}, ${valueIdentifier})`
      )
    } else if (figmaProp.kind === 'boolean') {
      dynamicPropLines.push(
        `const ${valueIdentifier} = instance.getBoolean(${createStringLiteral(figmaProp.figmaProp)})`
      )
      dynamicPropLines.push(
        `const ${propIdentifier} = ${valueIdentifier} ? ${createStringLiteral(` ${figmaProp.prop}`)} : ''`
      )
    } else if (figmaProp.kind === 'string') {
      usesStringPropHelper = true
      dynamicPropLines.push(
        `const ${valueIdentifier} = instance.getString(${createStringLiteral(figmaProp.figmaProp)})`
      )
      dynamicPropLines.push(
        `const ${propIdentifier} = createStringProp(${createStringLiteral(figmaProp.prop)}, ${valueIdentifier})`
      )
    }

    dynamicPropInterpolations.push(`\${${propIdentifier}}`)
  }

  let dynamicChildren

  if (hasFigmaChildren) {
    validateFigmaChildren(component.id, component.figmaChildren)
    const childrenIdentifier = createIdentifier(
      usedIdentifiers,
      component.figmaChildren.variable ?? 'children'
    )
    const fallback = component.figmaChildren.fallback
    const fallbackExpression =
      fallback === undefined ? '' : ` || ${createStringLiteral(fallback)}`

    dynamicPropLines.push(
      `const ${childrenIdentifier} = instance.getString(${createStringLiteral(component.figmaChildren.figmaProp)})${fallbackExpression}`
    )
    dynamicChildren = `\${${childrenIdentifier}}`
  }

  const preludeLines = ['const instance = figma.selectedInstance']

  if (usesStringPropHelper) {
    preludeLines.push(
      '',
      'function createStringProp(name, value) {',
      "  return value === undefined || value === ''",
      "    ? ''",
      '    : ` ${name}=${JSON.stringify(String(value))}`',
      '}'
    )
  }

  preludeLines.push('', ...dynamicPropLines)

  return {
    children: dynamicChildren,
    hasDynamicParts,
    prelude: `${preludeLines.join('\n')}\n`,
    props: dynamicPropInterpolations.join(''),
  }
}

function createExample(component, props, children, selfClosing) {
  if (selfClosing) {
    return `figma.code\`<${component}${props} />\``
  }

  return `figma.code\`<${component}${props}>${children}</${component}>\``
}

function validateFigmaProp(componentId, figmaProp) {
  if (!['enum', 'boolean', 'string'].includes(figmaProp?.kind)) {
    throw new Error(
      `Code Connect component ${componentId} has an invalid figmaProps kind.`
    )
  }

  if (
    typeof figmaProp.figmaProp !== 'string' ||
    figmaProp.figmaProp.length === 0 ||
    typeof figmaProp.prop !== 'string' ||
    figmaProp.prop.length === 0
  ) {
    throw new Error(
      `Code Connect component ${componentId} figmaProps entries need figmaProp and prop strings.`
    )
  }
}

function validateFigmaChildren(componentId, figmaChildren) {
  if (
    figmaChildren?.kind !== 'string' ||
    typeof figmaChildren.figmaProp !== 'string' ||
    figmaChildren.figmaProp.length === 0
  ) {
    throw new Error(
      `Code Connect component ${componentId} figmaChildren must map a string figmaProp.`
    )
  }
}

function createEnumOptions(figmaProp) {
  const values = { ...(figmaProp.values ?? {}) }

  if (
    figmaProp.default !== undefined &&
    !Object.hasOwn(values, figmaProp.default)
  ) {
    values[figmaProp.default] = undefined
  }

  return createObjectLiteral(values)
}

function createObjectLiteral(values) {
  const entries = Object.entries(values)

  if (entries.length === 0) {
    return '{}'
  }

  return `{
${entries
  .map(
    ([key, value]) =>
      `  ${createPropertyKey(key)}: ${value === undefined || value === null || value === '' ? 'undefined' : createStringLiteral(value)},`
  )
  .join('\n')}
}`
}

function createPropertyKey(key) {
  if (/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)) {
    return key
  }

  return createStringLiteral(key)
}

function createStringLiteral(value) {
  const json = JSON.stringify(String(value))
  const body = json.slice(1, -1).replace(/'/g, "\\'")

  return `'${body}'`
}

function createIdentifier(usedIdentifiers, name, suffix = '') {
  const words = String(name)
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)

  const base =
    words.length === 0
      ? 'value'
      : words
          .map((word, index) =>
            index === 0
              ? word[0].toLowerCase() + word.slice(1)
              : word[0].toUpperCase() + word.slice(1)
          )
          .join('')
  const suffixName = suffix ? suffix[0].toUpperCase() + suffix.slice(1) : ''
  const identifier = `${/^[a-zA-Z_$]/.test(base) ? base : `value${base}`}${suffixName}`

  if (!usedIdentifiers.has(identifier)) {
    usedIdentifiers.add(identifier)
    return identifier
  }

  let counter = 2
  let nextIdentifier = `${identifier}${counter}`

  while (usedIdentifiers.has(nextIdentifier)) {
    counter += 1
    nextIdentifier = `${identifier}${counter}`
  }

  usedIdentifiers.add(nextIdentifier)
  return nextIdentifier
}

function validateComponentId(id) {
  if (typeof id !== 'string' || id.length === 0) {
    throw new Error('Code Connect component id must be a non-empty string.')
  }

  if (path.basename(id) !== id || /[\\/]/.test(id) || id.includes('..')) {
    throw new Error(`Invalid Code Connect component id found: ${id}`)
  }

  return id
}

function escapeTemplateLiteral(value) {
  return String(value).replace(/`/g, '\\`').replace(/\$\{/g, '\\${')
}

function isPublishableFigmaUrl(url) {
  return (
    typeof url === 'string' &&
    /^https:\/\/www\.figma\.com\/(file|design)\//.test(url) &&
    url.includes('node-id=')
  )
}

function getFigmaUrl(component) {
  if (component.figmaUrl) {
    return component.figmaUrl
  }

  if (!figmaFileUrl || !component.nodeId) {
    return ''
  }

  const url = new URL(figmaFileUrl)
  url.searchParams.set('node-id', component.nodeId)
  url.searchParams.set('m', 'dev')

  return url.toString()
}
